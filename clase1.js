
// Para escribir información en consola
console.log('Hola mundo');

// Definir variable
var nombre;

// Asignar valor

nombre = 'valor';

// Para asignar textos, en javascript está la convención de usar
// las comillas simples
nombre = 'Ignacio';

// Javascript no necesita punto y coma. Es opcional, pero es una convención
// Hay casos muy puntuales donde es obligatorio el punto y coma

// Escribir una concatenación en el log
console.log('Hola ' + nombre);

// Asignar un valor en la misma línea
var apellido = 'Quinteros';

// Concatenación
console.log('Hola ' + nombre + ' ' + apellido);

